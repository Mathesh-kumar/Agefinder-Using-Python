# Function to print board numbers in square shape
def printBoard(i):
    print('\n', "=" * 25, "Board", i+1, "=" * 25)
    board = eval('board'+str(i))
    for i in range(0,len(board),6):
        print(" "*20 , '|'.join([str(j) for j in board[i:i+6]]))

